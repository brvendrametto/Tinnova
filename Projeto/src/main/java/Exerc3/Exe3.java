package Exerc3;

public class Exe3 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int fatorial = 0; 
        
        for (int aux = fatorial-1; aux>0; aux--){
            fatorial = fatorial * aux;
        }
        
        System.out.println(fatorial);
    }
    
}
